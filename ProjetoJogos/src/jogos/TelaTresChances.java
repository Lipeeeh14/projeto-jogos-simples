    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        //====================
        MetodosGerais inst = new MetodosGerais();
        numSort = inst.sorteiaNum(1, 10);
        txtMensagem.setText("Número sorteado entre 1 a 10, tente adivinhar!");
        
        acertou = false;
        vidas = 3;
        //====================
    }//GEN-LAST:event_btnIniciarActionPerformed
    
    private void txtNumActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtNumActionPerformed
        //=================
        try {
            boolean valida;
            
            chute = Integer.parseInt(txtNum.getText());
            valida = validaJogo();
            if(valida){
                vidas--;
                if (chute == numSort){
                    JOptionPane.showMessageDialog(null, "Parabéns! Missão Cumprida!");
                    acertou = true;
                    numSort = 0; // reseta a variável do número sorteado
                } else if(vidas > 0){
                    txtMensagem.setText("Tente outra vez, você ainda tem "+ vidas +" vidas");
                } else {
                    JOptionPane.showMessageDialog(null, "Game Over!");
                    txtMensagem.setText("O número sorteado é: "+ numSort);
                }    
            }
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, "Número inválido, digite novamente");
        }    
        //=================
    }//GEN-LAST:event_txtNumActionPerformed

