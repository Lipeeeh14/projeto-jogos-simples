    private boolean validaJogo(){
        boolean situacao = true;
        
        if(chute > 10 || chute < 1){
            JOptionPane.showMessageDialog(null, "O número tem que estar entre 1 e 10!");
            situacao = false;
        } else if(numSort == 0){
            JOptionPane.showMessageDialog(null, "Para começar clique no botão Iniciar!");
            situacao = false;
        } else if(acertou || vidas == 0){
            JOptionPane.showMessageDialog(null, "Está rodada já acabou, clique em Iniciar para jogar novamente!");
            situacao = false;
        }
        return situacao;
    }    
    
